"""
primitive data types
consist of: numbers, boolean, string
"""

# immutable
print("\nPrimitive Data Types are Immutable")
x = 1
print(id(x), " ", type(x))
x = "oke"
print(id(x), " ", type(x))

# numbers
print("\nPrimitive: Numbers")
x = 6
print(x, " ", type(x))
x = 6.0
print(x, " ", type(x))
x = 1+2j
print(x, " ", type(x))

# boolean
print("\nPrimitive: Boolean")
x = True
print(x, " ", type(x))
x = False
print(x, " ", type(x))

